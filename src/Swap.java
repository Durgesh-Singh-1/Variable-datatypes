public class Swap {
    public static void main(String[] args) {
        int num1= 2;
        int num2= 3;
        System.out.println("Before swap :- " + num1+ "," +num2);
        int swap;
        swap=num1;
        num1=num2;
        num2=swap;
        System.out.println(" Afet swap :- " + num1 + "," + num2);

    }
}
